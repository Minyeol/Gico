package org.techtown.gico;

public class SmallUnit {
    String smallUnitNumber;
    String smallUnitName;

    public SmallUnit(String smallUnitNumber, String smallUnitName) {
        this.smallUnitNumber = smallUnitNumber;
        this.smallUnitName = smallUnitName;
    }

    public String getSmallUnitNumber() {
        return smallUnitNumber;
    }

    public void setSmallUnitNumber(String smallUnitNumber) {
        this.smallUnitNumber = smallUnitNumber;
    }

    public String getSmallUnitName() {
        return smallUnitName;
    }

    public void setSmallUnitName(String smallUnitName) {
        this.smallUnitName = smallUnitName;
    }
}
